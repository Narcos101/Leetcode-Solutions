//Function to find the data of nth node from the end of a linked list.
int getNthFromLast(Node *head, int n)
{
    Node *ptr = head;
   
   int nodeCount = 0;
   
   while(ptr != nullptr){
       nodeCount++;
       ptr = ptr -> next;
   }
   
   int loc = nodeCount - n;
   
   ptr = head;
   
   int temp = -1, i = 0;
   
   while(ptr != nullptr)
   {
       if(i == loc){
           temp = ptr -> data;
           break;
       }
       i++;
       ptr = ptr -> next;
   }
   
   if(n > nodeCount)
       return -1;
   else
       return temp;


}

